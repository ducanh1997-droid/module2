package Introduce;

public class variable {
    public static void main(String[] args) {
        int a = 10;
        float f = 20.5F;
        double d = 20.5D;
        boolean b = true;
        char c = 'a';
        String s  = "Hà Nội";
        System.out.println(a);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
        System.out.println(f);
    }
}
